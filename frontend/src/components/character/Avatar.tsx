const renderGroups = (groups: PixelGroup[], palette: Record<PaletteKey, string>) =>
  groups.flatMap((group) =>
    group.pixels.map(([x, y]) => (
      <rect
        key={`${group.color}-${x}-${y}`}
        x={x * PIXEL_SIZE}
        y={y * PIXEL_SIZE}
        width={PIXEL_SIZE}
        height={PIXEL_SIZE}
        fill={palette[group.color]}
      />
    )),
  );

export const Avatar: React.FC<AvatarProps> = ({ data, size = 'md', className }) => {
  const sizes = { sm: 80, md: 160, lg: 240 };
  const spriteWidth = GRID_WIDTH * PIXEL_SIZE;
  const spriteHeight = GRID_HEIGHT * PIXEL_SIZE;
  const outfit = outfitPalettes[data.outfit] || outfitPalettes['outfit-1'];

  const palette: Record<PaletteKey, string> = {
    outline: '#10151b',
    skin: data.skinTone,
    skinShadow: shadeHex(data.skinTone, -18),
    hair: data.hairColor,
    hairShadow: shadeHex(data.hairColor, -22),
    eye: data.eyes.color,
    eyeWhite: '#f2efe6',
    outfit: outfit.main,
    outfitShadow: outfit.shadow,
    outfitAccent: outfit.accent,
    boot: '#1b1f24',
    mouth: '#b86d74',
    metal: '#5f708f',
    cloth: '#8a3131',
    highlight: '#d8efff',
  };

  const face = faceSprite(data.eyes.shape);
  const hair = hairSprites[data.hairStyle] || hairSprites['hair-1'];
  const clothes = outfitSprites[data.outfit] || outfitSprites['outfit-1'];
  const facialHair = facialHairSprites[data.facialHair] || facialHairSprites.none;
  const accessories = (data.accessories || []).flatMap((item) => accessorySprites[item] || []);

  return (
    <svg
      viewBox={`0 0 ${spriteWidth} ${spriteHeight}`}
      className={cn('h-full w-full', className)}
      style={{ width: sizes[size], height: sizes[size] * 1.25, shapeRendering: 'crispEdges' }}
      preserveAspectRatio="xMidYMid meet"
    >
      <rect width={spriteWidth} height={spriteHeight} fill="transparent" />
      {renderGroups(baseBodySprite, palette)}
      {renderGroups(face, palette)}
      {renderGroups(clothes, palette)}
      {renderGroups(bootsSprite, palette)}
      {renderGroups(facialHair, palette)}
      {renderGroups(hair, palette)}
      {renderGroups(accessories, palette)}
    </svg>
  );
};

